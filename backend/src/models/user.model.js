import mongoose from "mongoose";

// setting up the schema for the user table

const userSchema = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
    },
    fullName: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
      minLength: 6,
    },
    profilePic: {
      type: String,
      default: "",
    },
  },
  { timestamps: true }
);

const User = mongoose.model("User", userSchema); // Mongoose requires the name (in brackets) to be singular and first letter capitalised, and it will be appropriately updated in the real database
export default User;
